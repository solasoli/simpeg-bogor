		<h2>
			Pemberkasan Pelamar CPNS Kota Bogor Tahun 2014
		</h2>
		<p style="text-align: justify;">
			Dengan ini disampaikan kepada masyarakat yang telah melakukan registrasi online di sscn.bkn.go.id 
			terkait Penerimaan CPNS Kota Bogor Tahun 2014, untuk segera melengkapi berkas lamaran agar dapat mengikuti 
			Tes Penerimaan CPNS Kota Bogor tahun 2014. Berkas tersebut harus dikirimkan melalui jasa Pos dengan Layanan 
			POS EXPRESS ditujukan kepada Walikota Bogor melalui <strong>PO BOX 16000 Bogor</strong>, paling lambat tanggal <strong>30 September 2014</strong> Cap Pos. 
		</p>
		<p style="text-align: justify;">
			Adapun berkas yang harus dilampirkan adalah sebagai berikut :
		</p>
		<ol>
			<li>
				<p>Surat Lamaran ditulis tangan dengan  tinta hitam di atas kertas folio bergaris dan ditandatangani di atas materai Rp. 6000,- ditujukan kepada Walikota Bogor.</p>
			</li>
			<li>				
				<p>
				Foto copy ijazah dan Akta IV (bagi tenaga pendidik) beserta transkrip nilai yang sah yang dikeluarkan dari Sekolah/ Perguruan Tinggi yang terakreditasi atau telah mendapat ijin operasional/ penyelenggaraan dari DIRJEN DIKTI KEMENDIKNAS, serta dilegalisir/ disahkan oleh Pejabat yang berwenang.
				</p>
			</li>
			<li>
				<p>
				Bagi Pelamar dengan Ijazah yang belum tercantum status akreditasinya, agar melampirkan Surat Keterangan dari perguruan tinggi/ fakultas terkait status akreditasi pada tahun kelulusan.
				</p>
			</li>
			<li>
				<p>
				Pas photo berwarna ukuran 4 x 6 cm sebanyak 3 ( tiga ) lembar (Tulis nama dan tanggal lahir di belakang pas photo).
				</p>
			</li>
			<li>
				<p>
				Fotokopi  Kartu Tanda Penduduk (KTP) yang masih berlaku.
				</p>
			</li>
			<li>
				<p>
				Bukti register pendaftaan/ Hasil Cetak nomor registrasi on line BKN.
				</p>
			</li>
			<li>
				<p>
				Khusus untuk pelamar formasi dokter, bidan dan perawat harus melampirkan foto copy Surat Tanda Registrasi (STR) dan Ijazah Ners untuk formasi S1 Keperawatan (Ners).
				</p>
			</li>
			<li>
				<p>
				Khusus bagi pelamar Tenaga Pendidik (Guru) yang berusia lebih dari 35 (tiga puluh lima) tahun sampai  dengan 40 (empat puluh) tahun (per 01 Januari 2015) harus melampirkan bukti pengangkatan dan bukti telah bekerja sekurang-kurangnya 17 tahun 8 bulan per 01 Januari 2015 secara terus menerus pada lembaga pendidikan negeri atau swasta di wilayah Kota Bogor, yang dilegalisir oleh pimpinan unit kerjanya
				</p>
			</li>
			<li>
				<p>
				Tulis Kode Formasi pada sampul lamaran di pojok kanan atas. (Kode Formasi dapat dilihat di web kota bogor <a href="kotabogor.go.id" target="_blank">www.kotabogor.go.id</a> )
				</p>
			</li>
		</ol>
		<p style="text-align: center;">
			<img src="http://new.kotabogor.go.id/uploads/images/Berita/gambar11 11.jpg" alt="gambar11 11" />
		</p>
		<p style="text-align: justify;">
			Seluruh berkas lamaran dimasukkan kedalam map berwarna Biru untuk Tenaga Strategis/ Teknis, Merah untuk Tenaga Kesehatan dan Hijau untuk Tenaga Pendidik. Disertai dengan amplop balasan berwarna putih yang telah ditempel perangko Rp. 5.000,- (lima ribu rupiah). Surat balasan tersebut bertuliskan alamat rumah lengkap pelamar dan nomor telepon yang dapat dihubungi. 
		</p>
