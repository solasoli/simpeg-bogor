<div>
	<h2>
		TATA TERTIB PESERTA TES KEMAMPUAN DASAR (TKD)
	</h2>
	<ol type="A" class="text-justify">
		<li>
			TATA TERTIB PESERTA
			<ol type="1">
				<li>Peserta hadir paling lambat 60 (enam puluh) menit sebelum tes dimulai.</li>
				<li>Peserta wajib mengisi daftar hadir yang telah disiapkan oleh panitia.</li>
				<li>Peserta wajib membawa KTP (asli dan fotocopy) serta Katu Peserta ujian untuk diserahkan pada panitia.</li>
				<li>Peserta harus sesuai dengan foto yang ada di kartu peserta.</li>
				<li>Peserta berpakaian rapi dan sopan (tidak memakai kaos, celana jeans serta sandal)</li>
				<li>Peserta duduk pada tempat yang telah ditentukan.</li>
				<li>Peserta yang terlambat tidak diperkenankan masuk ruang tes dan mengikuti tes (dianggap gugur).</li>
				<li>Peserta hanya diperbolehkan membawa Kartu Peserta, KTP dan alat tulis berupa pensil ke dalam ruang tes.</li>
				<li>Peserta di dalam ruang tes dilarang membawa :
					<ol type="a">
						<li>Buku-buku dan catatan lainnya.</li>
						<li>Kalkulator, teleponn genggam (HP), kamera dalam bentuk apapun.</li>
						<li>Makanan dan minuman.</li>
						<li>Senjata api/tajam atau sejenisnya</li>
					</ol>
				</li>
				<li>Peserta dilarang :
					<ol type="a">
						<li>Bertanya/berbicara dengan sesama peserta tes.</li>
						<li>Menerima/Memberikan sesuatu dari/kepada peserta lain tanpa seijin panitia selama ujian.</li>
						<li>Keluar ruangan, kecuali memperoleh ijin dari panitia</li>
						<li>Merokok di lokasi tes</li>						
					</ol>
				</li>
			</ol>
		</li>
		<li>SANKSI
			<ol type="1">
				<li>Pelanggaran tata tertib diberikan sanksi berupa teguran lisan sampai dibatalkan sebagai peserta tes oleh panitia.</li>
				<li>Peserta tes yang kedapatan membawa alat komunikasi (HP), kamera dalam bentuk apapun dinyatakan gugur dan dikeluarkan dari ruangan tes.</li>
			</ol>
		</li>
		<li>LAIN-LAIN
			<ul>
				<li>Hal-hal lain yang belum tercantum dalam tata tertib ini akan di atur kemudian dan merupakan tata tertib tambahan. yang langsung disahkan</li>
			</ul>
		</li>
	</ol>
</div>
