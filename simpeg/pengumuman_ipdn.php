	diusulkan mendapatkan Tanda Kehormatan Satyalancana Karya Satya 20 tahun (perak) dengan 
	menyertakan pula persyaratan administrasi lainnya.
	</p>
	<p>
	<b>Ilustrasi II : </b><br/>
	Seorang PNS bernama “B” ,  NIP . 19780904 200701 1 001  dan memiliki Peninjauan Masa Kerja (PMK)  
	5 tahun , meskipun yang bersangkutan  sudah memiliki masa kerja sebelumnya  akan tetapi untuk hal 
	ini masa kerja tersebut tidak dihitung, maka terhitung per  1 Maret 2012 yang bersangkutan memiliki 
	masa kerja  5 Tahun 2  Bulan, . karena TMT masa kerja adalah TMT masa kerja pertama kali yang 
	bersangkutan diangkat menjadi CPNS yaitu 1 Januari 2007,  sehingga yang bersangkutan belum 
	berhak untuk  diusulkan  mendapatkan Tanda Kehormatan  Satyalancana Karyasatya.  
